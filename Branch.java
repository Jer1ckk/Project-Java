public class Branch {
    int branchID;
    String branchName;
    String branchLocation;
    String managerName;
    String contact;
    
    public Branch(int branchID, String branchName, String branchLocation, String managerName, String contact) {
        this.branchID = branchID;
        this.branchName = branchName;
        this.branchLocation = branchLocation;
        this.managerName = managerName;
        this.contact = contact;
    }
}
